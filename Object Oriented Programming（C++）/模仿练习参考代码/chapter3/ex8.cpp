/*
有如下一道算术题，被雨淋湿了，9个数字中只能看清4个，
但第一个方格尽管看不清，但肯定不是1，请编程把看不清的5个数字找出来。
(□x(□3+□))^2 = 8□□9
*/
#include <iostream.h>
void main()
{
	int a,b,c,d,e,x,y;
	for(a=1;a<10;a++)
		for(b=1;b<10;b++)
			for(c=1;c<10;c++)
				for(d=0;d<10;d++)
					for(e=0;e<10;e++)
					{   
						x = a*(b*10+3+c);
						x *=x;
						y = 8*1000+d*100+e*10+9;
						if(x == y)
							cout<<"a="<<a<<",b="<<b<<",c="<<c<<",d="<<d<<",e="<<e<<endl;
					}
}