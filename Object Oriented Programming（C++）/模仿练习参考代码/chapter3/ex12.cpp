/*
	马虎的算式:  小明是个急性子，上小学的时候经常把老师写在黑板上的题目抄错。    
	有一次，老师出的题目是：36 x 495 = ? 
 	他却给抄成了：396 x 45 = ?
    但结果却很戏剧性，他的答案竟然是对的！！
因为 36 * 495 = 396 * 45 = 17820
类似这样的巧合情况可能还有很多，比如：27 * 594 = 297 * 54。
    假设 a b c d e 代表1～9不同的5个数字（注意是各不相同的数字，且不含0），
	能满足形如： ab * cde = adb * ce 这样的算式一共有多少种呢？
*/
#include "iostream.h"
void main()
{  
    int a,b,c,d,e,x,y,icount=0;
	for(a=1;a<10;a++)
	for(b=1;b<10;b++)
	for(c=1;c<10;c++)
	for(d=1;d<10;d++)
	for(e=1;e<10;e++)
	{
		x = (a*10+b) * (c*100+d*10+e);
		y = (a*100+d*10+b) * (c*10+e);
		if(x == y)
		if(a!=b && a!=c && a!=d && a!=e &&
		   b!=c && b!=d && b!=e &&
		   c!=d && c!=e &&
		   d!=e)
		{   icount++;
			cout<<a<<b<<"*"<<c<<d<<e<<"="<<a<<d<<b<<"*"<<c<<e<<endl;
		}
	}
	cout<<"满足形如： ab * cde = adb * ce 这样的算式一共有"<<icount<<endl;
}

