#include <iostream.h>
void main()
{   //1.  设 a = 10，b = 3，计算表达式a-b+++1的值。
	int a =10,b=3,c;
	c = a-b+++1;     //相当于 c = a-(b++)+1->c = 10-3+1 =8;b = b+1=4
	cout<<"a-b+++1="<<c<<",b="<<b<<",a="<<a<<endl;
    //2.  输入一个3位数的整数，编写一个程序，将它的十位数和百位数互换位置。
	int x,y,x1,x2,x3;
	cout<<"输入一个3位数的整数:";
	cin>>x;
	x1=x/100;
	x2= (x/10)%10;
	x3=x%10;
	y = x3*100+x2*10+x1;
    cout<<y<<endl;
}
