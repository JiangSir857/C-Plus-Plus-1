#include "iostream.h"
void main()
{  
	//1．设a = 10, b = 3, c = 10,且a *= b = c-2;计算a,b,c的值。
	int a=10, b=3, c=10;
	a *= b = c-2;
	cout<<"a="<<a<<",b="<<b<<",c="<<c<<endl;
	
	//2．设计一个程序计算a*=7*3-15和a*=b*=5+4的值，并分析执行过程。
	a=10;
	a*=7*3-15;
	cout<<"a*=7*3-15="<<a<<endl;
	a=10;
	b=3;
	a*=b*=5+4;
	cout<<"a*=b*=5+4="<<a<<endl;
}
